public class Ejercicio_4 {

	/*
	 * Declarar dos variables, asignar un valor cualquiera y realizar las siguientes operaciones:
	 * Mostrar la suma
	 * Mostrar la multiplicacion
	 * Mostrar la resta
	 * Mostrar Division
	 */
	public static void main(String[] args) {
		
		double valor1 = 35.6;
		double valor2 = 12.5;
		
		double suma = valor1 + valor2;
		System.out.println(valor1 + " + " + valor2 + " = " + suma);
		
		double multiplicacion = valor1 * valor2;
		System.out.println(valor1 + " * " + valor2 + " = " + multiplicacion);
		
		double resta = valor1 - valor2;
		System.out.println(valor1 + " - " + valor2 + " = " + resta);
		
		double division = valor1 / valor2;
		System.out.println(valor1 + " / " + valor2 + " = " + division);
		
		
		
		

	}

}
